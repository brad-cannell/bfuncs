## ------------------------------------------------------------------------
library(tidyverse)
library(bfuncs)

## ------------------------------------------------------------------------
data(mtcars)

## ------------------------------------------------------------------------
about_data(mtcars)

## ------------------------------------------------------------------------
mtcars %>% 
  mean_table(mpg)

## ------------------------------------------------------------------------
alpha <- 1 - .99
t <- 1 - alpha / 2

mtcars %>% 
  mean_table(mpg, t_prob = t)

## ------------------------------------------------------------------------
mtcars %>% 
  mean_table(mpg, output = "all", digits = 5)

## ------------------------------------------------------------------------
mtcars %>% 
  group_by(cyl) %>% 
  mean_table(mpg, output = "all", digits = 5)

## ------------------------------------------------------------------------
mtcars %>% 
  group_by(am) %>% 
  freq_table(output = "all", digits = 5)

## ------------------------------------------------------------------------
mtcars %>% 
  group_by(am) %>% 
  freq_table(ci_type = "wald", output = "all", digits = 5)

## ------------------------------------------------------------------------
mtcars %>% 
  group_by(am, cyl) %>% 
  freq_table(output = "all", digits = 5)

